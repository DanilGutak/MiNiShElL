#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	len;
	size_t	i;

	i = 0;
	len = ft_strlen(dst);
	if (len > dstsize)
		return (ft_strlen(src) + dstsize);
	dstsize = dstsize - len;
	while (src[i] && dstsize-- > 1)
	{
		dst[len + i] = src[i];
		i++;
	}
	dst[len + i] = '\0';
	return (len + ft_strlen(src));
}
